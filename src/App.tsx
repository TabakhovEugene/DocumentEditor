const App: React.FC = () => {
  const [blocks, setBlocks] = useState<Block[]>(INITIAL_DATA);

  const handleUpdateBlock = (id: string, updates: Partial<Block>) => {
    setBlocks((prevBlocks) =>
      prevBlocks.map((block) =>
        block.id === id ? { ...block, ...updates } : block
      )
    );
  };

  const handleMoveBlock = (index: number, direction: 'up' | 'down') => {
    setBlocks((prevBlocks) => {
      const newBlocks = [...prevBlocks];

      if (direction === 'up' && index > 0) {
        [newBlocks[index], newBlocks[index - 1]] = [newBlocks[index - 1], newBlocks[index]];
      } else if (direction === 'down' && index < newBlocks.length - 1) {
        [newBlocks[index], newBlocks[index + 1]] = [newBlocks[index + 1], newBlocks[index]];
      }

      return newBlocks;
    });
  };

  return (
    <div className="app-container">
      <aside className="app-sidebar">
        <Navigator blocks={blocks} />
      </aside>
      <main className="app-content">
        <Editor blocks={blocks} onUpdateBlock={handleUpdateBlock} onMoveBlock={handleMoveBlock}/>
      </main>
    </div>
  );
};

export default App;